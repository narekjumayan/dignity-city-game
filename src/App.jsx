function getCharacterOutcomes(episodeOutcomes, memories) {
    const openedMemoryIds = new Set(memories.map((memory) => memory.id));

    return memorySlots.map((slot) => {
        const savedOutcome = episodeOutcomes[slot.id];

        if (savedOutcome) {
            return {
                characterName: slot.shortTitle,
                ...savedOutcome,
            };
        }

        if (openedMemoryIds.has(slot.id)) {
            return {
                characterId: slot.id,
                characterName: slot.shortTitle,
                status: "Պատմությունը բացվեց",
                tone: "open",
                text: fallbackOutcomeTexts[slot.id],
                dignityPreserved: true,
                storyClosed: false,
            };
        }

        return {
            characterId: slot.id,
            characterName: slot.shortTitle,
            status: "Պատմությունը փակ մնաց",
            tone: "closed",
            text: fallbackOutcomeTexts[slot.id],
            dignityPreserved: false,
            storyClosed: true,
        };
    });
}

